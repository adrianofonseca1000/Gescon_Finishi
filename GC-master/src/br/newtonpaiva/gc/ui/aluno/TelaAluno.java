    public void exibirDados(Aluno a) {
        //Iguala a classe que veio a variavel global
        alunoEdicao = a;
        //seta os valores aos campos
        edtCpf.setText(a.getCpf());
        edtEmail.setText(a.getEmail());
        edtNomeAluno.setText(a.getNome());
        edtRa.setText(a.getRa());
        if (a.getDeficiente().equals("S")) {
            cbbDeficiente.setSelectedItem(Aluno.DEFICIENTE);
        } else {
            cbbDeficiente.setSelectedItem(Aluno.NAO_DEFICIENTE);
        }
        cbbCurso.setSelectedItem(a.getCurso().getCurso());
        btnLimpar.setEnabled(false);
    }

    private void Limpar() {
        //Limpa os campos
        edtCpf.setText("");
        edtEmail.setText("");
        edtNomeAluno.setText("");
        edtRa.setText("");
        cbbCurso.setSelectedIndex(0);
        cbbDeficiente.setSelectedIndex(0);
    }

    private void CursosComponente() {
        try {
            List<Curso> lista = Curso.buscarTodos();
            // Limpa o componente
            cbbCurso.removeAllItems();
            cbbCurso.addItem("<<Selecione um item>>");
            lista.forEach(emp -> {
                cbbCurso.addItem(emp.getCurso());
            });
        } catch (SQLException ex) {
            Logger.getLogger(TelaContrato.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
