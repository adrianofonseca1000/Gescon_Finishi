    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String Inserir = "N";
        //Se o curso edição é nulo
        if (alunoEdicao == null) {
            //Inicia uma nova classe
            alunoEdicao = new Aluno();
            Inserir = "S";
        }
        //Alterar o campo deficiente
        String deficiente = "";
        if (cbbDeficiente.getSelectedIndex() == 1) {
            deficiente = Aluno.NAO_DEFICIENTE;
        } else if (cbbDeficiente.getSelectedIndex() == 2) {
            deficiente = Aluno.DEFICIENTE;
        }
        //Inicia uma nova classe de curso
        Curso c = new Curso();
        try {
            if (cbbCurso.getSelectedIndex() > 0) {

                List<Curso> lista = Curso.buscarPorNome((String) cbbCurso.getSelectedItem());
                if (lista.size() < 1) {
                    MensagemUtil.exibirDialogo("curso.nao.existe");
                    return;
                }
                lista.forEach(Cursos -> {
                    c.setId(Cursos.getId());
                });
            } else {
                MensagemUtil.exibirDialogo("curso.nao.existe");
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaAluno.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        //Seta o nome do aluno para salvar (Inserir ou editar)
        alunoEdicao.setNome(edtNomeAluno.getText());
        alunoEdicao.setCpf(edtCpf.getText());
        alunoEdicao.setCurso(c);
        alunoEdicao.setDeficiente(deficiente);
        alunoEdicao.setEmail(edtEmail.getText());
        alunoEdicao.setRa(edtRa.getText());
        try {
            //Salvar
            alunoEdicao.salvar();
            //Mensagem de acordo com a açãso (Edição ou inserção)
            if (Inserir.equals("S")) {
                MensagemUtil.exibirDialogo("aluno.ok.inserir");
                Limpar();
                alunoEdicao = null;
            } else {
                MensagemUtil.exibirDialogo("aluno.ok.alterar");
            }
        } catch (AlunoInvalidoException ex) {
            cbbCurso.requestFocus();
            Logger.getLogger(TelaAluno.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (SQLException ex) {
            Logger.getLogger(TelaAluno.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

